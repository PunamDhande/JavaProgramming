package basic_to_advance;

public class SwapTwoNumbersUsingThirdVariable {

	public static void main(String[] args) {
			int a,b,temp;
			a=10;
			b=20;
			System.out.println("Before swapping a="+a+ "and b="+b);
			temp=a;
			a=b;
			b=temp;
			System.out.println("After swapping a="+a+ "and b="+b);

	}

}
